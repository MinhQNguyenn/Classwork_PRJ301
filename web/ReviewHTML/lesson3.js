function Tong(a, b) {
    var c = parseInt(a.value) + parseInt(b.value);
    return c;
}

function Hieu(a, b) {
    var c = parseInt(a.value) - parseInt(b.value);
    return c;
}

function Tich(a, b) {
    var c = parseInt(a.value) * parseInt(b.value);
    return c;
}

function Thuong(a, b) {
    var c = parseInt(a.value) / parseInt(b.value);
    return c;
}


